let first = Math.floor(Math.random()*100+1);
let second = Math.floor(Math.random()*50+1);

console.log(`${first} + ${second} = ${first+second}`);
console.log(`${first} - ${second} = ${first-second}`);
console.log(`${first} * ${second} = ${first*second}`);
console.log(`${first} / ${second} = ${first/second}`);